    private void btnCreate1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreate1ActionPerformed
        String ten = txtTen1.getText().trim();
        String diaChi = txtDiaChi.getText().trim();
        String sdt = txtSDT.getText().trim();
        Matcher matcher1 = Pattern.compile(P_SDT).matcher(txtSDT.getText());
        if (ten.length() == 0 || diaChi.length() == 0 || sdt.length() == 0) {
            JOptionPane.showMessageDialog(this, "Không được để trống");
        } else if (!matcher1.matches()) {
            JOptionPane.showMessageDialog(this, "Số điện thoại phải có 10 kí tự là số");
        } else {
            NhaCungCapReponse ncc = new NhaCungCapReponse();
            ncc.setTenNCC(ten);
            ncc.setDiaChi(diaChi);
            ncc.setSdt(sdt);
            quanLyNCCService.add(ncc);
            JOptionPane.showMessageDialog(this, "Thêm thành công");
            loadData();
        }
    }//GEN-LAST:event_btnCreate1ActionPerformed

